# 실수 계산하기
print(3.5+2.1)  # 5.6
print(4.3-2.7)  # 1.5999999999999996
# 4.3-2.7은 1.6이 나와야 하는데 1.5999999999999996이 출력됨
# 이유는 컴퓨터는 실수를 표현할 때 오차가 발생됨 오차 문제는 다소 어려운 주제이므로 현재는 신경 쓸 필요 없음(실수 계산 결과를 비교 할 때는 == 연산자를 사용하면 안됨)
print(1.5*3.1)  # 4.65
print(5.5/3.1)  # 1.7741935483870968
print(4.2+5)  # 9.2 (실수와 정수를 더해본다.)
